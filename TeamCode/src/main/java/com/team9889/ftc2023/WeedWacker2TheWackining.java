package com.team9889.ftc2023;

import com.qualcomm.robotcore.eventloop.opmode.LinearOpMode;
import com.qualcomm.robotcore.eventloop.opmode.TeleOp;
import com.qualcomm.robotcore.hardware.DcMotor;

import com.qualcomm.robotcore.hardware.Servo;


@TeleOp
public class WeedWacker2TheWackining extends LinearOpMode {

  Robit mRobot=new Robit();
  boolean intakeOn=false;
  boolean intakeToggle=true;

    @Override
    public void runOpMode() throws InterruptedException {

        mRobot.init(hardwareMap);
        waitForStart();

        while(opModeIsActive()){
            mRobot.mDrive.setPower(gamepad1.left_stick_y,gamepad1.right_stick_y );
            if (gamepad1.a && intakeToggle){
                mRobot.mIntake.on();
                intakeOn=! intakeOn;
                intakeToggle=false;

            }
            else if (! gamepad1.a){
                intakeToggle=true;
            }
            if (intakeOn) mRobot.mIntake.on();
            else mRobot.mIntake.off();

            if (gamepad1.b)
            {mRobot.mIntake.out();}



            if(gamepad1.right_trigger > 0.1){
                mRobot.mHopper.on();}
            else if(gamepad1.left_trigger > 0.1){
                mRobot.mHopper.out();}
            else{
                mRobot.mHopper.off();}



            if (gamepad1.right_bumper){
                mRobot.mLift.up();}
            else if(gamepad1.left_bumper){
                mRobot.mLift.down();}
            else{
                mRobot.mLift.off();}



//            if(gamepad1.right_trigger > 0.1){
//                else if
//            }
        }
    }
}

















































































































































































































































































































































































































































































